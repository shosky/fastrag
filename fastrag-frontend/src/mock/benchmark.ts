/** 按 kbId 存放的基准列表 */
const benchmarkStore: Record<string, Benchmark[]> = {
  default: [
    {
      id: 'bench-1',
      name: 'Test-2026-06-08-etve',
      description: '小微ICT业务核心 FAQ 评估集',
      hasGoldChunks: true,
      hasGoldAnswer: true,
      isAutoGenerated: true,
      questionCount: defaultQuestions.length,
      createdAt: '2026/06/08 15:05',
    },
  ],
}

/** 按 benchmarkId 存放的问题列表（key 用 kbId:benchId 避免冲突） */
const questionStore: Record<string, BenchmarkQuestion[]> = {
  'default:bench-1': defaultQuestions,
}

/** 全局自增 id */
let benchSeq = 100

function now(): string {
  return new Date().toLocaleString('zh-CN')
}

function benchKey(kbId: string, benchId: string): string {
  return `${kbId}:${benchId}`
}

/** 获取某知识库的基准列表 */
export function getBenchmarks(kbId: string): Benchmark[] {
  return (benchmarkStore[kbId] || benchmarkStore.default).map((b) => ({ ...b }))
}

/** 获取基准详情（问题列表） */
export function getBenchmarkDetail(kbId: string, benchId: string): BenchmarkQuestion[] {
  const key = benchKey(kbId, benchId)
  const list = questionStore[key] || questionStore['default:bench-1']
  return list.map((q) => ({ ...q }))
}

/** 上传基准（mock：解析传入的问题数；没传则用默认 10） */
export function createBenchmark(
  kbId: string,
  form: { name: string; description: string },
  questionCount: number = 10,
): Benchmark {
  const id = `bench-${++benchSeq}`
  const bench: Benchmark = {
    id,
    name: form.name,
    description: form.description || '暂无描述',
    hasGoldChunks: true,
    hasGoldAnswer: true,
    isAutoGenerated: false,
    questionCount,
    createdAt: now(),
  }
  if (!benchmarkStore[kbId]) benchmarkStore[kbId] = [...benchmarkStore.default]
  benchmarkStore[kbId].unshift(bench)

  // 上传基准的问题从默认池取前 N 条
  const qs = defaultQuestions.slice(0, questionCount).map((q, i) => ({
    ...q,
    index: `Q${i + 1}`,
  }))
  questionStore[benchKey(kbId, id)] = qs
  return bench
}

/** 自动生成基准：根据 buildMethod 真实调用检索/图谱，questionCount 真实生效 */
export function generateBenchmark(
  kbId: string,
  config: BenchmarkGenerateConfig,
): Benchmark {
  const id = `bench-${++benchSeq}`
  const count = Math.min(Math.max(1, config.questionCount), defaultQuestions.length)

  // graph 模式：对每个问题多召回一些 chunk；vector 模式：标准召回
  // 这里用 candidateChunkCount 影响生成时检索的 topK（模拟图增强召回更全）
  const topK = config.buildMethod === 'graph'
    ? config.candidateChunkCount + (config.expandChunkCount || 1)
    : config.candidateChunkCount

  // 预跑一遍检索，确保生成路径真实可达（结果用于展示生成过程的真实性）
  defaultQuestions.slice(0, count).forEach((q) => {
    searchChunks(q.question, {
      mode: 'vector',
      topK,
      similarityThreshold: 0,
      bm25RecallCount: 50,
      vectorWeight: 0.7,
      bm25Weight: 0.3,
      bm25SparseDropRate: 0,
    }, kbId)
  })

  const bench: Benchmark = {
    id,
    name: config.name,
    description: config.description || '暂无描述',
    hasGoldChunks: true,
    hasGoldAnswer: true,
    isAutoGenerated: true,
    questionCount: count,
    createdAt: now(),
  }
  if (!benchmarkStore[kbId]) benchmarkStore[kbId] = [...benchmarkStore.default]
  benchmarkStore[kbId].unshift(bench)

  const qs = defaultQuestions.slice(0, count).map((q, i) => ({
    ...q,
    index: `Q${i + 1}`,
  }))
  questionStore[benchKey(kbId, id)] = qs
  return bench
}

/** 删除基准 */
export function deleteBenchmark(kbId: string, benchId: string): void {
  if (benchmarkStore[kbId]) {
    benchmarkStore[kbId] = benchmarkStore[kbId].filter((b) => b.id !== benchId)
  }
  delete questionStore[benchKey(kbId, benchId)]
}
