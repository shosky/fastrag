import type { RetrievalConfig } from './knowledge'

// ===========================================================================
// 知识图谱
// ===========================================================================

/** 图谱节点（数据层，不含渲染坐标） */
export interface GraphNode {
  id: string
  name: string
  /** 实体类型/标签，例如「服务」「客户类型」 */
  label: string
  type: 'entity' | 'chunk'
  /** kb 标识 */
  kbId?: string
}

/** 图谱关系（source/target 用实体名称，与 GraphRelation 对齐） */
export interface GraphEdge {
  source: string
  target: string
  label: string
}

/** 实体类型聚合统计 */
export interface EntityType {
  name: string
  count: number
  color: string
}

/** 图谱视图节点 = 数据节点 + 渲染属性 */
export interface GraphViewNode extends GraphNode {
  /** SVG 内部坐标，由确定性布局算法计算 */
  x: number
  y: number
  color: string
  size: number
}

/** 图谱整体数据 */
export interface GraphData {
  nodes: GraphNode[]
  edges: GraphEdge[]
}

/** 图谱统计 */
export interface GraphStats {
  entityCount: number
  relationCount: number
  entityTypes: EntityType[]
}

// ===========================================================================
// 检索
// ===========================================================================

/** 单条检索结果（与 SearchTestPanel 内的 SearchResult 对齐） */
export interface SearchResultItem {
  index: number
  /** 相似度百分比，例如 37.83 */
  similarity: number
  content: string
  source: string
  fileId: string
  chunkIndex: number
  /** 距离 0-1 */
  distance: number
  /** 命中的查询关键词（用于高亮展示） */
  highlights?: string[]
  /** 带高亮标记的预览片段（HTML，命中的词用 <mark> 包裹） */
  previewSnippet?: string
}

/** 检索请求 */
export interface RetrievalRequest {
  knowledgeId: string
  query: string
  config: RetrievalConfig
}

// ===========================================================================
// 评估基准
// ===========================================================================

/** 评估基准 */
export interface Benchmark {
  id: string
  name: string
  description: string
  hasGoldChunks: boolean
  hasGoldAnswer: boolean
  isAutoGenerated: boolean
  questionCount: number
  createdAt: string
}

/** 基准中的单个问题 */
export interface BenchmarkQuestion {
  index: string
  question: string
  /** gold chunk 标识，例如 file_1be435_chunk_10 */
  goldChunks: string
  /** gold 答案文本 */
  goldAnswer: string
}

/** 基准生成方式 */
export type BenchmarkBuildMethod = 'vector' | 'graph'

/** 基准生成参数 */
export interface BenchmarkGenerateConfig {
  name: string
  description: string
  buildMethod: BenchmarkBuildMethod
  llmModel: string
  questionCount: number
  candidateChunkCount: number
  concurrency: number
  /** graph 模式专属：每轮扩展 chunk 数 */
  expandChunkCount?: number
}

// ===========================================================================
// RAG 评估
// ===========================================================================

export type EvaluationStatus = 'pending' | 'running' | 'completed' | 'failed'

/** 评估记录（列表用） */
export interface Evaluation {
  id: string
  name: string
  benchmark: string
  benchmarkCount: number
  dataCount: number
  completedCount: number
  duration: string
  recallAt10: number
  overallScore: number
  status: EvaluationStatus
  createdAt: string
  runId: string
}

/** 单个问题的评估结果 */
export interface EvaluationResult {
  question: string
  /** 模型拼接生成的答案 */
  generatedAnswer: string
  /** 检索指标文本，例如「R@1 1.000  R@3 1.000」 */
  retrievalMetrics: string
  /** 答案评判结果 */
  answerJudgment: AnswerJudgeResult
}

/** 答案评判结果 */
export interface AnswerJudgeResult {
  /** 是否正确 */
  isCorrect: boolean
  /** 评判说明 */
  reason: string
}

/** 评估详情 */
export interface EvaluationDetail {
  id: string
  name: string
  runId: string
  status: string
  overallScore: number
  totalQuestions: number
  completedCount: number
  duration: string
  recallAt1: number
  recallAt3: number
  recallAt5: number
  recallAt10: number
  /** 答案准确率 0-1 */
  answerAccuracy: number
  results: EvaluationResult[]
}

/** 评估启动配置 */
export interface EvaluationStartConfig {
  name: string
  benchmark: string
  answerModel: string
  judgeModel: string
}
