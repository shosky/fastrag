package com.fastrag.module.graph.service.impl;
import com.baomidou.mybatisplus.core.conditions.query.LambdaQueryWrapper;
import com.fastrag.module.graph.entity.KbBenchmark; import com.fastrag.module.graph.mapper.KbBenchmarkMapper;
import com.fastrag.module.graph.service.BenchmarkService;
import lombok.RequiredArgsConstructor; import org.springframework.stereotype.Service;
import java.util.*;
@Service @RequiredArgsConstructor
public class BenchmarkServiceImpl implements BenchmarkService {
    private final KbBenchmarkMapper mapper;
    @Override public List<KbBenchmark> list(String kbId) { return mapper.selectList(new LambdaQueryWrapper<KbBenchmark>().eq(KbBenchmark::getKbId,kbId).orderByDesc(KbBenchmark::getCreatedAt)); }
    @Override public KbBenchmark getDetail(String kbId,String id) { return mapper.selectById(id); }
    @Override public KbBenchmark create(String kbId,Map<String,Object> form) { var b=new KbBenchmark(); b.setKbId(kbId); b.setName((String)form.get("name")); b.setDescription((String)form.get("description")); b.setQuestionCount(form.get("questionCount")instanceof Integer?(int)form.get("questionCount"):0); b.setIsAutoGenerated(0); mapper.insert(b); return b; }
    @Override public void generate(String kbId,Map<String,Object> config) { var b=new KbBenchmark(); b.setKbId(kbId); b.setName((String)config.get("name")); b.setIsAutoGenerated(1); b.setQuestionCount(config.get("questionCount")instanceof Integer?(int)config.get("questionCount"):10); mapper.insert(b); }
    @Override public void delete(String kbId,String id) { mapper.deleteById(id); }
}
