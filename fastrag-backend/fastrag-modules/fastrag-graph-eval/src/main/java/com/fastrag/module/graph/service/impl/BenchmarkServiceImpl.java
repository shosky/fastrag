package com.fastrag.module.graph.service.impl;

import cn.hutool.json.JSONUtil;
import com.baomidou.mybatisplus.core.conditions.query.LambdaQueryWrapper;
import com.fastrag.ai.llm.LlmService;
import com.fastrag.module.graph.entity.KbBenchmark;
import com.fastrag.module.graph.entity.KbBenchmarkQuestion;
import com.fastrag.module.graph.mapper.KbBenchmarkMapper;
import com.fastrag.module.graph.mapper.KbBenchmarkQuestionMapper;
import com.fastrag.module.graph.service.BenchmarkService;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.scheduling.annotation.Async;
import org.springframework.stereotype.Service;

import java.util.*;

@Slf4j
@Service
@RequiredArgsConstructor
public class BenchmarkServiceImpl implements BenchmarkService {

    private final KbBenchmarkMapper mapper;
    private final KbBenchmarkQuestionMapper questionMapper;
    private final LlmService llmService;

    @Override
    public List<KbBenchmark> list(String kbId) {
        return mapper.selectList(new LambdaQueryWrapper<KbBenchmark>()
                .eq(KbBenchmark::getKbId, kbId)
                .orderByDesc(KbBenchmark::getCreatedAt));
    }

    @Override
    public KbBenchmark getDetail(String kbId, String id) {
        return mapper.selectById(id);
    }

    @Override
    public KbBenchmark create(String kbId, Map<String, Object> form) {
        KbBenchmark b = new KbBenchmark();
        b.setKbId(kbId);
        b.setName((String) form.get("name"));
        b.setDescription((String) form.get("description"));
        b.setQuestionCount(form.get("questionCount") instanceof Integer ? (int) form.get("questionCount") : 0);
        b.setIsAutoGenerated(0);
        mapper.insert(b);
        return b;
    }

    @Override
    public void generate(String kbId, Map<String, Object> config) {
        KbBenchmark b = new KbBenchmark();
        b.setKbId(kbId);
        b.setName((String) config.get("name"));
        b.setDescription((String) config.get("description"));
        b.setIsAutoGenerated(1);
        int questionCount = config.get("questionCount") instanceof Integer ? (int) config.get("questionCount") : 10;
        b.setQuestionCount(questionCount);
        mapper.insert(b);

        generateQuestions(b.getId(), kbId, questionCount);
    }

    @Override
    public void delete(String kbId, String id) {
        questionMapper.delete(new LambdaQueryWrapper<KbBenchmarkQuestion>()
                .eq(KbBenchmarkQuestion::getBenchmarkId, id));
        mapper.deleteById(id);
    }

    @Async
    protected void generateQuestions(String benchmarkId, String kbId, int questionCount) {
        try {
            String prompt = String.format("""
                生成 %d 个通用知识问答对，用于测试RAG系统的检索和回答能力。
                返回JSON数组：[{"question":"问题","goldAnswer":"标准答案"}]
                """, questionCount);

            String response = llmService.chat(null, prompt);
            String json = response.trim();
            if (json.startsWith("```")) {
                json = json.replaceAll("```json?", "").replaceAll("```", "").trim();
            }

            @SuppressWarnings("unchecked")
            List<Map<String, String>> questions = (List<Map<String, String>>) (List<?>) JSONUtil.toList(json, Map.class);

            for (int i = 0; i < questions.size(); i++) {
                Map<String, String> q = questions.get(i);
                KbBenchmarkQuestion question = new KbBenchmarkQuestion();
                question.setBenchmarkId(benchmarkId);
                question.setQuestionIndex(i);
                question.setQuestion(q.get("question"));
                question.setGoldAnswer(q.get("goldAnswer"));
                questionMapper.insert(question);
            }

            KbBenchmark benchmark = mapper.selectById(benchmarkId);
            if (benchmark != null) {
                benchmark.setQuestionCount(questions.size());
                benchmark.setHasGoldAnswer(1);
                mapper.updateById(benchmark);
            }

            log.info("Generated {} questions for benchmark: {}", questions.size(), benchmarkId);

        } catch (Exception e) {
            log.error("Failed to generate questions for benchmark: {}", benchmarkId, e);
        }
    }
}
